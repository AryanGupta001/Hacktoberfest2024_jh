let box1 = document.querySelector("#box1");
let love = document.querySelector("#i1");

box1.addEventListener('dblclick',()=>{
    love.style.transform= "translate(-50% , -50%) scale(1)";
    setTimeout(() => {
        love.style.transform= "translate(-50% , -50%) scale(0)";
    }, 600);
})
